# operadores relacionales 
# Igual que 
print(23 == 21)
# Distinto que
print(23 != 23) 
# Mayor que
print(2 > 1)
# Menor que 
print(2 < 1)
# Mayor o igual que
print(12 >= 10)
# Menor o igual que
print(12 <= 11) 
print("_____________")

# Validar el valor lógico a través de variables
a = 2
b = 4
print(a == b, 
      a != b,
      a <= b)
